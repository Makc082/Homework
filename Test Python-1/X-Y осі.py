x1 = int(input("Векдіть кооординати x1 осі: "))
y1 = int(input("Введіть кооординати y1 осі: "))
x2 = int(input("Введіть кооординати x2 осі: "))
y2 = int(input("Введіть кооординати y2 осі: "))

if x1 == x2:
    print("Пряма буде паралельною Y-осі")
elif y1 == y2:
    print("Пряма буде паралельною x-осі")
else:
    print("Пряма не є паралельною ані X-осі, ані Y-осі.")