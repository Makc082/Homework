#d - депозит , t - термін вкладу , p - відсоток річних , a - відсоток на місяць , x - відсоток за зазначений період.
d = float(input("Введіть суму (в гривнях): "))
t = int(input("Введіть термін вкладу: "))
p = float(input("Введіть відсоток річних: "))
a = p / 12 / 100
x = round(d * a, 2) * t
print("Виплата за термін вкладу: " , x)