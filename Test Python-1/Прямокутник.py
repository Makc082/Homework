x1 = int(input("Введіть x координати вехнього лівого кута: "))
y1 = int(input("Введіть y координати вехнього лівого кута: "))
x2 = int(input("Введіть x координати нижнього правого кута: "))
y2 = int(input("Введіть y координати нижнього правого кута: "))
x = int(input("Введіть x координати точки: "))
y = int(input("Введіть y координати точки: "))

if x1 <= x <= x2 and y1 <= y <= y2:
    print("Належить")
else:
    print("Не належить")
